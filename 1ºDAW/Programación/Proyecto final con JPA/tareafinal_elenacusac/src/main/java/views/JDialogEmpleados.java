    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        String idTexto = txtIdEmpleado.getText().trim();
        String nombre = txtNombre.getText().trim().toLowerCase();
        String ape1 = txtApe1.getText().trim().toLowerCase();
        String ape2 = txtApe2.getText().trim().toLowerCase();
        String dni = txtDni.getText().trim().toLowerCase();
        String email = txtEmail.getText().trim().toLowerCase();
        String telefono = txtTelefono.getText().trim().toLowerCase();
        String rol = txtRol.getSelectedItem().toString();
        String horario = txtHorario.getText().trim().toLowerCase();
        String sueldo = txtSueldo.getText().trim().toLowerCase();

        List<Empleados> lista = controller.findAll();

        boolean encontrado = false;

        for (Empleados e : lista) {

            boolean coincide = true;

            if (!idTexto.isEmpty()) {

                String idEmpleado = String.valueOf(e.getCodEmpleado());

                if (!idEmpleado.contains(idTexto)) {
                    coincide = false;
                }
            }

            if (!nombre.isEmpty() && !e.getNombre().toLowerCase().contains(nombre)) {
                coincide = false;
            }

            if (!ape1.isEmpty() && !e.getApe1emp().toLowerCase().contains(ape1)) {
                coincide = false;
            }

            if (!ape2.isEmpty()) {

                String ape2emp = "";

                if (e.getApe2emp() != null) {
                    ape2emp = e.getApe2emp().toLowerCase();
                }

                if (!ape2emp.contains(ape2)) {
                    coincide = false;
                }
            }

            if (!dni.isEmpty() && !e.getDni().toLowerCase().contains(dni)) {
                coincide = false;
            }

            if (!email.isEmpty() && !e.getEmail().toLowerCase().contains(email)) {
                coincide = false;
            }

            if (!telefono.isEmpty() && !e.getTelefono().toLowerCase().contains(telefono)) {
                coincide = false;
            }

            if (!rol.isEmpty() && !e.getRol().toLowerCase().contains(rol)) {
                coincide = false;
            }

            if (!horario.isEmpty()) {
                String horarioEmp = "";

                if (e.getHorario() != null) {
                    horarioEmp = e.getHorario().toLowerCase();
                }

                if (!horarioEmp.contains(horario)) {
                    coincide = false;
                }
            }

            if (!sueldo.isEmpty()) {
                String sueldoEmp = String.valueOf(e.getSueldo());

                if (!sueldoEmp.contains(sueldo)) {
                    coincide = false;
                }
            }

            if (coincide) {
                modelo.addRow(new Object[]{
                    e.getCodEmpleado(),
                    e.getNombre(),
                    e.getApe1emp(),
                    e.getApe2emp(),
                    e.getRol(),
                    e.getHorario(),
                    e.getSueldo(),
                    e.getDni(),
                    e.getEmail(),
                    e.getTelefono()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

