    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un empleado.");
            return;
        }

        try {

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            Empleados e = controller.findById(id);
            
            String nombre = txtNombre.getText().trim();
            String ape1 = txtApe1.getText().trim();
            String ape2 = txtApe2.getText().trim();
            String rol = txtRol.getSelectedItem().toString();
            String horario = txtHorario.getText().trim();
            String sueldo = txtSueldo.getText().trim();
            String dni = txtDni.getText().trim();
            String email = txtEmail.getText().trim();
            String telefono = txtTelefono.getText().trim();

            if (nombre.isEmpty() || ape1.isEmpty() || rol.equals("-- Selecciona Rol --") || sueldo.isEmpty()
                    || horario.isEmpty() || dni.isEmpty() || email.isEmpty() || telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }

            if (!validarDatos(dni, telefono, email)) {
                return;
            }
            if (!txtIdEmpleado.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID del empleado no se puede insertar.");
                return;
            }

            e.setNombre(nombre);
            e.setApe1emp(ape1);
            e.setApe2emp(ape2);
            e.setRol(rol);
            e.setHorario(horario);
            e.setSueldo(Double.parseDouble(sueldo));
            e.setDni(dni);
            e.setEmail(email);
            e.setTelefono(telefono);

            controller.update(e);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Empleado actualizado correctamente.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un empleado.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar el empleado?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Empleado eliminado");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el empleado.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

