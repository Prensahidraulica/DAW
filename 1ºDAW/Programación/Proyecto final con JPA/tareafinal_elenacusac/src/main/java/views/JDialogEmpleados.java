    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            Empleados e = new Empleados();

            String nombre = txtNombre.getText().trim();
            String ape1 = txtApe1.getText().trim();
            String ape2 = txtApe2.getText().trim();
            String rol = txtRol.getSelectedItem().toString();
            String horario = txtHorario.getText().trim();
            String sueldo = txtSueldo.getText().trim();
            String dni = txtDni.getText().trim();
            String email = txtEmail.getText().trim();
            String telefono = txtTelefono.getText().trim();

            if (nombre.isEmpty() || ape1.isEmpty() || rol.equals("-- Selecciona Rol --") || sueldo.isEmpty()
                    || horario.isEmpty() || dni.isEmpty() || email.isEmpty()
                    || telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            if (!txtIdEmpleado.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID del empleado no se puede insertar.");
                return;
            }

            if (!validarDatos(dni, telefono, email)) {
                return;
            }

            e.setNombre(nombre);
            e.setApe1emp(ape1);
            e.setApe2emp(ape2);
            e.setRol(rol);
            e.setHorario(horario);
            e.setSueldo(Double.parseDouble(sueldo));
            e.setDni(dni);
            e.setEmail(email);
            e.setTelefono(telefono);

            controller.create(e);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Empleado insertado correctamente.");
            cargarTabla();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

