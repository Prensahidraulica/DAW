    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un servicio contratado.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar el servicio contratado?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Servicio contratado eliminado.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el servicio contratado.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una contratación de un servicio.");
            return;
        }

        try {
            int selCliente = txtIdCliente.getSelectedIndex();
            int selServicio = txtIdServicio.getSelectedIndex();

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            DetalleServicios d = controller.findById(id);

            String cantidad = txtCantidad.getText().trim();
            String observaciones = txtObservaciones.getText().trim();

            // VALIDACIONES
            if (cantidad.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            if (selCliente == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un cliente.");
                return;
            }
            if (selServicio == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un servicio.");
                return;
            }
            if (!txtIdDetalle.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID del detalle no se puede actualizar.");
                return;
            }


            // ACTUALIZAR DATOS
            d.setCodCliente(selCliente);
            d.setCodServicio(selServicio);
            d.setCantidad(Integer.parseInt(cantidad));
            d.setObservaciones(observaciones);

            controller.update(d);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Contratación del servicio actualizada correctamente.");

            limpiarCampos();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            int selCliente = txtIdCliente.getSelectedIndex();
            int selServicio = txtIdServicio.getSelectedIndex();

            DetalleServicios d = new DetalleServicios();

            String cantidad = txtCantidad.getText().trim();
            String observaciones = txtObservaciones.getText().trim();

            // VALIDACIONES
            if (cantidad.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            if (selCliente == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un cliente.");
                return;
            }
            if (selServicio == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un servicio.");
                return;
            }
            if (!txtIdDetalle.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID del detalle no se puede insertar.");
                return;
            }

            // SET DATOS
            d.setCodCliente(selCliente);
            d.setCodServicio(selServicio);
            d.setCantidad(Integer.parseInt(cantidad));
            d.setObservaciones(observaciones);

            controller.create(d);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Contratación del servicio insertada correctamente.");

            limpiarCampos();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        // LIMPIAR TABLA
        modelo.setRowCount(0);

        // CAMPOS FILTRO
        String idDetalle = txtIdDetalle.getText().trim();
        int selCliente = txtIdCliente.getSelectedIndex();
        String idCliente = selCliente > 0 ? String.valueOf(selCliente) : "";
        int selServicio = txtIdServicio.getSelectedIndex();
        String codServicio = selServicio > 0 ? String.valueOf(selServicio) : "";
        String cantidad = txtCantidad.getText().trim();
        String observaciones = txtObservaciones.getText().trim().toLowerCase();

        List<DetalleServicios> lista = controller.findAll();

        boolean encontrado = false;

        for (DetalleServicios d : lista) {

            boolean coincide = true;

            // ID DETALLE
            if (!idDetalle.isEmpty()) {
                String id = String.valueOf(d.getCodDetalleServicio());
                if (!id.contains(idDetalle)) {
                    coincide = false;
                }
            }

            // COD CLIENTE
            if (!idCliente.isEmpty()) {
                String cliente = String.valueOf(d.getCodCliente());
                if (!cliente.contains(idCliente)) {
                    coincide = false;
                }
            }

            // COD SERVICIO
            if (!codServicio.isEmpty()) {
                String servicio = String.valueOf(d.getCodServicio());
                if (!servicio.contains(codServicio)) {
                    coincide = false;
                }
            }

            // CANTIDAD
            if (!cantidad.isEmpty()) {
                String cant = String.valueOf(d.getCantidad());
                if (!cant.contains(cantidad)) {
                    coincide = false;
                }
            }

            // OBSERVACIONES
            if (!observaciones.isEmpty()) {

                String obs = "";

                if (d.getObservaciones() != null) {
                    obs = d.getObservaciones().toLowerCase();
                }

                if (!obs.contains(observaciones)) {
                    coincide = false;
                }
            }

            // SI COINCIDE
            if (coincide) {

                modelo.addRow(new Object[]{
                    d.getCodDetalleServicio(),
                    d.getCodCliente(),
                    d.getCodServicio(),
                    d.getCantidad(),
                    d.getObservaciones()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void cargarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        List<DetalleServicios> lista = controller.findByCodServicio(idServicio);

        for (DetalleServicios d : lista) {
            modelo.addRow(new Object[]{
                d.getCodDetalleServicio(),
                d.getCodCliente(),
                d.getCodServicio(),
                d.getCantidad(),
                d.getObservaciones()
            });
        }
    }

    private void limpiarCampos() {
        txtIdDetalle.setText("");
        txtIdCliente.setSelectedIndex(0);
        txtIdServicio.setSelectedIndex(0);
        txtCantidad.setText("");
        txtObservaciones.setText("");
        jTable1.clearSelection();
    }

