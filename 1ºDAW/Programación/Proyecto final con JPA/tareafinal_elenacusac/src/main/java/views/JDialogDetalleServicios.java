    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int fila = jTable1.getSelectedRow();

        if (fila != -1) {
            txtIdDetalle.setText(jTable1.getValueAt(fila, 0).toString());
            try {
                int idClienteVal = Integer.parseInt(jTable1.getValueAt(fila, 1).toString());
                txtIdCliente.setSelectedIndex(idClienteVal);
            } catch (Exception ex) {
                txtIdCliente.setSelectedIndex(0);
            }
            try {
                int idServicioVal = Integer.parseInt(jTable1.getValueAt(fila, 2).toString());
                txtIdServicio.setSelectedIndex(idServicioVal);
            } catch (Exception ex) {
                txtIdServicio.setSelectedIndex(0);
            }
            txtCantidad.setText(jTable1.getValueAt(fila, 3).toString());
            txtObservaciones.setText(jTable1.getValueAt(fila, 4).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

