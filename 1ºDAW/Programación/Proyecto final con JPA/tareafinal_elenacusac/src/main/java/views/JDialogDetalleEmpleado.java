    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un detalle del empleado.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar el detalle del empleado?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Detalle del empleado contratado eliminado.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el detalle del empleado.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una contratación de un servicio.");
            return;
        }
        if (!txtIdDetalle.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Recuerda que el ID del detalle no se puede actualizar.");
            return;
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            DetalleEmpleado d = controller.findById(id);

            String codEmpleado = "";
            int selEmpleado = txtIdEmpleado.getSelectedIndex();
            int selParque = txtIdParque.getSelectedIndex();
            String idParque = selParque > 0 ? String.valueOf(selParque) : "";
            String fechaInicio = txtFechaInicio.getText().trim();
            String fechaFin = txtFechaFin.getText().trim();
            String observaciones = txtObservaciones.getText().trim();

            // VALIDACIONES
            if (fechaInicio.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debes de introducir una fecha de inicio.");
                return;
            }

            if (selEmpleado == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un empleado.");
                return;
            }
            if (selParque == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un parque.");
                return;
            }

            // FECHA
            Date inicio = validarFecha(fechaInicio);
            if (inicio == null) {
                return;
            }

            Date fin = null;

            if (!fechaFin.isBlank()) {

                fin = validarFecha(fechaFin);

                if (fin == null) {
                    return;
                }
            }

            if (fin != null && fin.before(inicio)) {
                JOptionPane.showMessageDialog(this, "La fecha de fin no puede ser anterior a la fecha de inicio.");
                return;
            }

            // ACTUALIZAR DATOS
            d.setCodEmpleado(selEmpleado);
            d.setCodParque(selParque);
            d.setFechaInicio(inicio);
            d.setFechaFin(fin);
            d.setObservaciones(observaciones);

            controller.update(d);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Detalles del empleado actualizada correctamente.");

            limpiarCampos();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

