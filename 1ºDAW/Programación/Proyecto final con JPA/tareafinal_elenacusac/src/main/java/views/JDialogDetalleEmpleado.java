    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        // LIMPIAR TABLA
        modelo.setRowCount(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        // CAMPOS FILTRO
        String idDetalle = txtIdDetalle.getText().trim();
        int selEmpleado = txtIdEmpleado.getSelectedIndex();
        String codEmpleado = selEmpleado > 0 ? String.valueOf(selEmpleado) : "";
        int selParque = txtIdParque.getSelectedIndex();
        String codParque = selParque > 0 ? String.valueOf(selParque) : "";
        String fechaInicio = txtFechaInicio.getText().trim();
        String fechaFin = txtFechaFin.getText().trim();
        String observaciones = txtObservaciones.getText().trim();

        List<DetalleEmpleado> lista = controller.findAll();

        boolean encontrado = false;

        for (DetalleEmpleado d : lista) {

            boolean coincide = true;

            // ID DETALLE
            if (!idDetalle.isEmpty()) {
                String id = String.valueOf(d.getCodDetalle());

                if (!id.contains(idDetalle)) {
                    coincide = false;
                }
            }

            // COD EMPLEADO
            if (!codEmpleado.isEmpty()) {
                String empleado = String.valueOf(d.getCodEmpleado());

                if (!empleado.contains(codEmpleado)) {
                    coincide = false;
                }
            }

            // COD PARQUE
            if (!codParque.isEmpty()) {
                String empleado = String.valueOf(d.getCodParque());

                if (!empleado.contains(codParque)) {
                    coincide = false;
                }
            }

            // FECHA INICIO: aceptar formato dd/MM/yyyy
            if (!fechaInicio.isEmpty()) {
                boolean matchInicio = false;

                try {
                    java.util.Date buscInicio = sdf.parse(fechaInicio);
                    String buscInicioFmt = sdf.format(buscInicio);
                    String fechaEmp = d.getFechaInicio() != null ? sdf.format(d.getFechaInicio()) : "";

                    if (fechaEmp.equals(buscInicioFmt)) {
                        matchInicio = true;
                    }
                } catch (Exception ex) {
                    String fechaEmp = d.getFechaInicio() != null ? sdf.format(d.getFechaInicio()).toLowerCase() : "";
                    if (fechaEmp.contains(fechaInicio.toLowerCase())) {
                        matchInicio = true;
                    }
                }

                if (!matchInicio) {
                    coincide = false;
                }
            }

            // FECHA FIN: aceptar formato dd/MM/yyyy
            if (!fechaFin.isEmpty()) {
                boolean matchFin = false;

                try {
                    java.util.Date buscFin = sdf.parse(fechaFin);
                    String buscFinFmt = sdf.format(buscFin);
                    String fechaF = d.getFechaFin() != null ? sdf.format(d.getFechaFin()) : "";

                    if (fechaF.equals(buscFinFmt)) {
                        matchFin = true;
                    }
                } catch (Exception ex) {
                    String fechaF = d.getFechaFin() != null ? sdf.format(d.getFechaFin()).toLowerCase() : "";
                    if (fechaF.contains(fechaFin.toLowerCase())) {
                        matchFin = true;
                    }
                }

                if (!matchFin) {
                    coincide = false;
                }
            }

            // OBSERVACIONES
            if (!observaciones.isEmpty()) {

                String obs = "";

                if (d.getObservaciones() != null) {
                    obs = d.getObservaciones().toLowerCase();
                }

                if (!obs.contains(observaciones)) {
                    coincide = false;
                }
            }

            // SI COINCIDE
            if (coincide) {

                modelo.addRow(new Object[]{
                    d.getCodDetalle(),
                    d.getCodEmpleado(),
                    d.getCodParque(),
                    sdf.format(d.getFechaInicio()),
                    sdf.format(d.getFechaFin()),
                    d.getObservaciones()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void limpiarCampos() {
        txtIdDetalle.setText("");
        txtIdEmpleado.setSelectedIndex(0);
        txtIdParque.setSelectedIndex(0);
        txtFechaInicio.setText("");
        txtFechaFin.setText("");
        txtObservaciones.setText("");
        jTable1.clearSelection();
    }

    private void cargarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        List<DetalleEmpleado> lista = controller.findByCodEmpleado(idEmpleado);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (DetalleEmpleado d : lista) {

            String fechaInicio = "";
            String fechaFin = "";

            if (d.getFechaInicio() != null) {
                fechaInicio = sdf.format(d.getFechaInicio());
            }

            if (d.getFechaFin() != null) {
                fechaFin = sdf.format(d.getFechaFin());
            }

            modelo.addRow(new Object[]{
                d.getCodDetalle(),
                d.getCodEmpleado(),
                d.getCodParque(),
                fechaInicio,
                fechaFin,
                d.getObservaciones()
            });
        }
    }

    private Date validarFecha(String textoFecha) {

        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);

            return sdf.parse(textoFecha);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fecha incorrecta.\nFormato requerido: dd/MM/yyyy\nEjemplo: 04/06/2026");
            return null;
        }
    }

