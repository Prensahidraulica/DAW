    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una zona.");
            return;
        }

        try {

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            Zonas z = controller.findById(id);

            String nombre = txtNombre.getText().trim();
            String descripcion = txtDescripcion.getText().trim();
            int selParque = txtIdParque.getSelectedIndex();
            
            if (nombre.isEmpty() || selParque == 0) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            if (!txtZona.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID de la zona no se puede insertar.");
                return;
            } 
            
            z.setNombre(nombre);
            z.setCodParque(selParque);
            z.setDescripcion(descripcion);

            controller.update(z);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Zona actualizada correctamente.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una zona.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar la zona?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Zona eliminada.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar la zona.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

