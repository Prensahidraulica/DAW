    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        String idZona = txtZona.getText().trim();
        String nombre = txtNombre.getText().trim().toLowerCase();
        String descripcion = txtDescripcion.getText().trim().toLowerCase();
        int selParque = txtIdParque.getSelectedIndex();
        String codParque = selParque > 0 ? String.valueOf(selParque) : "";

        List<Zonas> lista = controller.findAll();

        boolean encontrado = false;

        for (Zonas z : lista) {

            boolean coincide = true;

            if (!idZona.isEmpty()) {

                String id = String.valueOf(z.getCodZona());

                if (!id.contains(idZona)) {
                    coincide = false;
                }
            }

            if (!nombre.isEmpty()
                    && !z.getNombre().toLowerCase().contains(nombre)) {

                coincide = false;
            }

            if (!descripcion.isEmpty()) {

                String desc = "";

                if (z.getDescripcion() != null) {
                    desc = z.getDescripcion().toLowerCase();
                }

                if (!desc.contains(descripcion)) {
                    coincide = false;
                }
            }

            if (!codParque.isEmpty()) {

                String parque = String.valueOf(z.getCodParque());

                if (!parque.contains(codParque)) {
                    coincide = false;
                }
            }

            if (coincide) {

                modelo.addRow(new Object[]{
                    z.getCodZona(),
                    z.getNombre(),
                    z.getDescripcion(),
                    z.getCodParque()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {

            JOptionPane.showMessageDialog(this,
                    "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

