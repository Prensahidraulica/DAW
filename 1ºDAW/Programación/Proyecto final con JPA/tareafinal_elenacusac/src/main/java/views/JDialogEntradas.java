    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        String idTexto = txtIdEntrada.getText().trim();
        String fecha = txtFecha.getText().trim().toLowerCase();
        String tipo = txtTipoEntrada.getSelectedItem().toString().toLowerCase();
        int selCliente = txtIdCliente.getSelectedIndex();
        String codCliente = selCliente > 0 ? String.valueOf(selCliente) : "";
        int selParque = txtIdParque.getSelectedIndex();
        String codParque = selParque > 0 ? String.valueOf(selParque) : "";

        List<Entradas> lista = controller.findAll();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        boolean encontrado = false;

        for (Entradas e : lista) {

            boolean coincide = true;

            if (!idTexto.isEmpty()) {

                String idEntrada = String.valueOf(e.getCodEntrada());

                if (!idEntrada.contains(idTexto)) {
                    coincide = false;
                }
            }

            validarFecha(fecha);
            String fechaFormateada = sdf.format(fecha);

            if (!fecha.isEmpty() && !fechaFormateada.contains(fecha)) {
                coincide = false;
            }

            if (!tipo.equals("-tipo entrada-")
                    && !e.getTipoEntrada().toLowerCase().contains(tipo)) {

                coincide = false;
            }

            if (!codCliente.isEmpty()) {

                String cliente = String.valueOf(e.getCodCliente());

                if (!cliente.contains(codCliente)) {
                    coincide = false;
                }
            }

            if (!codParque.isEmpty()) {

                String parque = String.valueOf(e.getCodParque());

                if (!parque.contains(codParque)) {
                    coincide = false;
                }
            }

            if (coincide) {

                modelo.addRow(new Object[]{
                    e.getCodEntrada(),
                    sdf.format(e.getFecha()),
                    e.getTipoEntrada(),
                    e.getCodParque(),
                    e.getCodCliente()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void cargarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        List<Entradas> lista = controller.findAll();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (Entradas e : lista) {

            modelo.addRow(new Object[]{
                e.getCodEntrada(),
                sdf.format(e.getFecha()),
                e.getTipoEntrada(),
                e.getCodParque(),
                e.getCodCliente()
            });
        }
    }

    private void limpiarCampos() {
        txtIdEntrada.setText("");
        txtFecha.setText("");
        txtTipoEntrada.setSelectedIndex(0);
        txtIdParque.setSelectedIndex(0);
        txtIdCliente.setSelectedIndex(0);
        jTable1.clearSelection();
    }
    
    private Date validarFecha(String textoFecha) {

        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);

            return sdf.parse(textoFecha);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fecha incorrecta.\nFormato requerido: dd/MM/yyyy\nEjemplo: 04/06/2026");
            return null;
        }
    }

