    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una entrada.");
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            Entradas e = controller.findById(id);

            String fechaTexto = txtFecha.getText().trim();

            // COMBOBOX
            String tipoEntrada = txtTipoEntrada.getSelectedItem().toString();
            int selParque = txtIdParque.getSelectedIndex();
            int selCliente = txtIdCliente.getSelectedIndex();

            // VALIDACIONES
            if (fechaTexto.isEmpty() || selParque == 0 || selCliente == 0 || tipoEntrada.equals("-Tipo Entrada-")) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            
            if (selParque == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un parque.");
                return;
            }
            if (selCliente == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un cliente.");
                return;
            }
            if (!txtIdEntrada.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID de la entrada no se puede actualizar.");
                return;
            }      

            // FECHA
            Date fecha = validarFecha(fechaTexto);

            // ACTUALIZAR DATOS
            e.setFecha(fecha);
            e.setTipoEntrada(tipoEntrada);
            e.setCodParque(selParque);
            e.setCodCliente(selCliente);

            controller.update(e);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Entrada actualizada correctamente.");

            limpiarCampos();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

