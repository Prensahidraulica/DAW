    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            int selZona = txtIdZona.getSelectedIndex();
            int selParque = txtIdParque.getSelectedIndex();
            
            if (selZona == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar una zona.");
                return;
            }
            if (selParque == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un parque.");
                return;
            }
            if(!txtIdAtraccion.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Recuerda que el ID de la atracción no se puede insertar.");
                return;
            }
            
            Atracciones a = new Atracciones();

            String nombre = txtNombre.getText().trim();
            String descripcion = txtDescripcion.getText().trim();
            String capacidadMaxima = txtCapacidadMaxima.getText().trim();
            String edadMinima = txtEdadMinima.getText().trim();
            String fechaInstalacion = txtFechaInstalacion.getText();
            Date fecha = validarFecha(fechaInstalacion);   

            if (nombre.isEmpty() || capacidadMaxima.isEmpty() || edadMinima.isEmpty() || fechaInstalacion.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }

            a.setNombre(nombre);            
            a.setFechaInstalacion(fecha);
            a.setDescripcion(descripcion);
            a.setCapacidadMaxima(Integer.parseInt(capacidadMaxima));
            a.setEdadMinima(Integer.parseInt(edadMinima));
            a.setCodZona(selZona);
            a.setCodParque(selParque);

            controller.create(a);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Atracción insertada correctamente.");
            limpiarCampos();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una atracción.");
            return;
        }

        try {            
            int selZona = txtIdZona.getSelectedIndex();
            int selParque = txtIdParque.getSelectedIndex();
            
            if (selZona == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar una zona.");
                return;
            }
            if (selParque == 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un parque.");
                return;
            }
            if(!txtIdAtraccion.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Recuerda que el ID de la atracción no se puede actualizar.");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            Atracciones a = controller.findById(id);

            String nombre = txtNombre.getText().trim();
            String fechaInstalacion = txtFechaInstalacion.getText().trim();
            Date fecha = validarFecha(fechaInstalacion);
            String descripcion = txtDescripcion.getText().trim();
            String edadMinima = txtEdadMinima.getText().trim();
            String capacidadMaxima = txtCapacidadMaxima.getText().trim();

            // VALIDACIONES
            if (nombre.isEmpty() || edadMinima.isEmpty() 
                    || capacidadMaxima.isEmpty()
                    || fechaInstalacion.isEmpty()) {

                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }
            

            Integer edad = Integer.parseInt(edadMinima);
            int capacidadMax = Integer.parseInt(capacidadMaxima);

            if (edad < 0) {
                JOptionPane.showMessageDialog(this, "La edad mínima no puede ser negativa.");
                return;
            }

            if (capacidadMax <= 0) {
                JOptionPane.showMessageDialog(this, "La capacidad debe ser mayor que 0.");
                return;
            }

            // ACTUALIZAR DATOS
            a.setNombre(nombre);
            a.setDescripcion(descripcion);
            a.setEdadMinima(edad);
            a.setCapacidadMaxima(capacidadMax);
            a.setFechaInstalacion(fecha);
            a.setCodZona(selZona);
            a.setCodParque(selParque);

            controller.update(a);

            cargarTabla();

            JOptionPane.showMessageDialog(this,
                    "Atracción actualizada correctamente.");
            limpiarCampos();

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this,
                    "Los campos numéricos tienen un formato incorrecto.");

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this,
                    ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una atracción.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar la atracción?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Atracción eliminada");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar la atracción.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

