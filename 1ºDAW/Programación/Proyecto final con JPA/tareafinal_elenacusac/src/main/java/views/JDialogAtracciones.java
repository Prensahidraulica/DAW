    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        // LIMPIAR TABLA
        modelo.setRowCount(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        // CAMPOS FILTRO
        String idAtraccion = txtIdAtraccion.getText().trim();
        String nombre = txtNombre.getText().trim().toLowerCase();
        String fechaInstalacion = txtFechaInstalacion.getText().trim();
        String descripcion = txtDescripcion.getText().trim().toLowerCase();
        String edadMinima = txtEdadMinima.getText().trim();
        String capacidadMaxima = txtCapacidadMaxima.getText().trim();

        validarFecha(fechaInstalacion);
        
        int selZona = txtIdZona.getSelectedIndex();
        int selParque = txtIdParque.getSelectedIndex();

        List<Atracciones> lista = controller.findAll();

        boolean encontrado = false;

        for (Atracciones a : lista) {

            boolean coincide = true;

            // ID ATRACCION
            if (!idAtraccion.isEmpty()) {
                String id = String.valueOf(a.getCodAtraccion());

                if (!id.contains(idAtraccion)) {
                    coincide = false;
                }
            }

            // NOMBRE
            if (!nombre.isEmpty()) {

                String nombreAtraccion = "";

                if (a.getNombre() != null) {
                    nombreAtraccion = a.getNombre().toLowerCase();
                }

                if (!nombreAtraccion.contains(nombre)) {
                    coincide = false;
                }
            }

            // FECHA INSTALACION
            if (!fechaInstalacion.isEmpty()) {

                String fechaAtraccion = "";

                if (a.getFechaInstalacion() != null) {
                    fechaAtraccion = sdf.format(a.getFechaInstalacion()).toLowerCase();
                }

                if (!fechaAtraccion.contains(fechaInstalacion)) {
                    coincide = false;
                }
            }

            // DESCRIPCION
            if (!descripcion.isEmpty()) {

                String desc = "";

                if (a.getDescripcion() != null) {
                    desc = a.getDescripcion().toLowerCase();
                }

                if (!desc.contains(descripcion)) {
                    coincide = false;
                }
            }

            // EDAD MINIMA
            if (!edadMinima.isEmpty()) {

                String edad = String.valueOf(a.getEdadMinima());

                if (!edad.contains(edadMinima)) {
                    coincide = false;
                }
            }

            // CAPACIDAD MAXIMA
            if (!capacidadMaxima.isEmpty()) {

                String capacidad = String.valueOf(a.getCapacidadMaxima());

                if (!capacidad.contains(capacidadMaxima)) {
                    coincide = false;
                }
            }

            // FILTRO ZONA (combo)
            if (selZona != 0) {
                try {
                    if (a.getCodZona() != selZona) {
                        coincide = false;
                    }
                } catch (Exception ex) {
                    coincide = false;
                }
            }

            // FILTRO PARQUE (combo)
            if (selParque != 0) {
                try {
                    if (a.getCodParque() != selParque) {
                        coincide = false;
                    }
                } catch (Exception ex) {
                    coincide = false;
                }
            }

            // SI COINCIDE → AÑADIR FILA
            if (coincide) {

                modelo.addRow(new Object[]{
                    a.getCodAtraccion(),
                    a.getNombre(),
                    sdf.format(a.getFechaInstalacion()),
                    a.getDescripcion(),
                    a.getCapacidadMaxima(),
                    a.getEdadMinima(),
                    a.getCodZona(),
                    a.getCodParque()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void cargarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0);

        List<Atracciones> lista = controller.findByCodParqueAndCodZona(idParque, idZona);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (Atracciones a : lista) {

            modelo.addRow(new Object[]{
                a.getCodAtraccion(),
                a.getNombre(),
                sdf.format(a.getFechaInstalacion()),
                a.getDescripcion(),
                a.getCapacidadMaxima(),
                a.getEdadMinima(),
                a.getCodZona(),
                a.getCodParque()
            });
        }
    }

