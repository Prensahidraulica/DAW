    private Date validarFecha(String textoFecha) {

        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);

            return sdf.parse(textoFecha);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fecha incorrecta.\nFormato requerido: dd/MM/yyyy\nEjemplo: 04/06/2026");
            return null;
        }
    }

