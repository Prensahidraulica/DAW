    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        // LIMPIAR TABLA
        modelo.setRowCount(0);

        // CAMPOS FILTRO
        String idTexto = txtIdCliente.getText().trim();
        String nombre = txtNombre.getText().trim().toLowerCase();
        String ape1 = txtApe1.getText().trim().toLowerCase();
        String ape2 = txtApe2.getText().trim().toLowerCase();
        String dni = txtDni.getText().trim().toLowerCase();
        String email = txtEmail.getText().trim().toLowerCase();
        String telefono = txtTelefono.getText().trim().toLowerCase();
        String fecha = txtFecNac.getText().trim();
        int selResponsable = txtCodResponsable.getSelectedIndex();

        List<Clientes> lista = controller.findAll();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        validarFecha(fecha);

        boolean encontrado = false;

        for (Clientes c : lista) {

            boolean coincide = true;

            // ID
            if (!idTexto.isEmpty()) {

                String idCliente = String.valueOf(c.getCodCliente());

                if (!idCliente.contains(idTexto)) {
                    coincide = false;
                }
            }

            // NOMBRE
            if (!nombre.isEmpty() && !c.getNombre().toLowerCase().contains(nombre)) {
                coincide = false;
            }

            // APELLIDO 1
            if (!ape1.isEmpty() && !c.getApe1cli().toLowerCase().contains(ape1)) {
                coincide = false;
            }

            // APELLIDO 2
            if (!ape2.isEmpty()) {

                String ape2cli = "";

                if (c.getApe2cli() != null) {
                    ape2cli = c.getApe2cli().toLowerCase();
                }

                if (!ape2cli.contains(ape2)) {
                    coincide = false;
                }
            }

            // DNI
            if (!dni.isEmpty() && !c.getDni().toLowerCase().contains(dni)) {
                coincide = false;
            }

            // EMAIL
            if (!email.isEmpty() && !c.getEmail().toLowerCase().contains(email)) {
                coincide = false;
            }

            // TELEFONO
            if (!telefono.isEmpty() && !c.getTelefono().toLowerCase().contains(telefono)) {
                coincide = false;
            }

            // FECHA: aceptar formato dd/MM/yyyy
            if (!fecha.isEmpty()) {
                boolean matchFecha = false;

                try {
                    java.util.Date fechaBuscada = sdf.parse(fecha);
                    String buscadaFormateada = sdf.format(fechaBuscada);
                    String fechaCliente = sdf.format(c.getFechaNacimiento());

                    if (fechaCliente.equals(buscadaFormateada)) {
                        matchFecha = true;
                    }
                } catch (Exception ex) {
                    // Si no se puede parsear, hacer búsqueda por contains sobre la cadena formateada
                    String fechaCliente = sdf.format(c.getFechaNacimiento()).toLowerCase();

                    if (fechaCliente.contains(fecha.toLowerCase())) {
                        matchFecha = true;
                    }
                }

                if (!matchFecha) {
                    coincide = false;
                }
            }

            // ID RESPONSABLE: aplicar filtro solo si se ha seleccionado uno
            if (selResponsable != 0) {
                String respCliente = "";

                if (c.getCodResponsable() != null) {
                    respCliente = String.valueOf(c.getCodResponsable());
                }

                if (!respCliente.contains(String.valueOf(selResponsable))) {
                    coincide = false;
                }
            }

            // SI COINCIDE → AÑADIR A TABLA
            if (coincide) {
                modelo.addRow(new Object[]{
                    c.getCodCliente(),
                    c.getNombre(),
                    c.getApe1cli(),
                    c.getApe2cli(),
                    c.getDni(),
                    sdf.format(c.getFechaNacimiento()),
                    c.getEmail(),
                    c.getTelefono(),
                    c.getCodResponsable()
                });

                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

