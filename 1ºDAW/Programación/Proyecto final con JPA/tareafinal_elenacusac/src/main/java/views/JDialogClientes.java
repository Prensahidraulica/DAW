    private Date validarFecha(String textoFecha) {

        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);

            return sdf.parse(textoFecha);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Fecha incorrecta.\nFormato requerido: dd/MM/yyyy\nEjemplo: 04/06/2026", "Error de fecha", JOptionPane.ERROR_MESSAGE);

            return null;
        }
    }

    private boolean validarDatos(String dni, String telefono, String correo) {
        String regexDNI = "^[0-9]{8}[A-Za-z]$";
        String regexTelefono = "^[6789][0-9]{8}$";
        String regexEmail = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";

        if (!dni.matches(regexDNI)) {
            JOptionPane.showMessageDialog(this, "DNI incorrecto. Formato: 12345678Z");
            return false;
        }

        if (!telefono.matches(regexTelefono)) {
            JOptionPane.showMessageDialog(this, "Teléfono incorrecto. Debe tener 9 cifras.");
            return false;
        }

        if (!correo.matches(regexEmail)) {
            JOptionPane.showMessageDialog(this, "Correo electrónico incorrecto.");
            return false;
        }

        return true;
    }

