    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            Clientes c = new Clientes();

            String nombre = txtNombre.getText();
            String ape1 = txtApe1.getText();
            String ape2 = txtApe2.getText();
            String dni = txtDni.getText();
            String telefono = txtTelefono.getText();
            String email = txtEmail.getText();
            String fechaNacimiento = txtFecNac.getText();

            // FECHA
            Date fecha = validarFecha(fechaNacimiento);

            if (fecha == null) {
                return;
            }

            if (!txtIdCliente.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Recuerda que el ID del cliente no se puede insertar.");
                return;
            }

            // CALCULAR EDAD
            java.util.Calendar hoy = java.util.Calendar.getInstance();
            java.util.Calendar nacimiento = java.util.Calendar.getInstance();
            nacimiento.setTime(fecha);

            int edad = hoy.get(java.util.Calendar.YEAR) - nacimiento.get(java.util.Calendar.YEAR);

            // SI ES MENOR → responsable obligatorio
            if (edad < 18) {
                int selResponsable = txtCodResponsable.getSelectedIndex();
                if (selResponsable == 0) {
                    JOptionPane.showMessageDialog(this, "Los menores necesitan responsable. Debes seleccionar uno.");
                    return;
                }

                Integer idResponsable = selResponsable;

                // BUSCAR RESPONSABLE
                Clientes responsable = controller.findById(idResponsable);

                // COMPROBAR QUE EXISTE
                if (responsable == null) {
                    JOptionPane.showMessageDialog(this, "El responsable no existe.");
                    return;
                }

                // CALCULAR EDAD DEL RESPONSABLE
                Calendar hoyResponsable = Calendar.getInstance();

                Calendar nacimientoResponsable = Calendar.getInstance();

                nacimientoResponsable.setTime(responsable.getFechaNacimiento());

                int edadResponsable = hoyResponsable.get(Calendar.YEAR) - nacimientoResponsable.get(Calendar.YEAR);

                // SI EL RESPONSABLE ES MENOR
                if (edadResponsable < 18) {
                    JOptionPane.showMessageDialog(this, "El responsable debe ser mayor de edad.");
                    return;
                }

                c.setCodResponsable(idResponsable);

            } else {
                c.setCodResponsable(null);
            }

            if (!validarDatos(dni, telefono, email)) {
                return;
            }

            if (!nombre.isEmpty() && !ape1.isEmpty() && !dni.isEmpty() && !telefono.isEmpty() && !email.isEmpty() && !fechaNacimiento.isEmpty()) {
                c.setNombre(nombre);
                c.setApe1cli(ape1);
                c.setApe2cli(ape2);
                c.setDni(dni);
                c.setTelefono(telefono);
                c.setEmail(email);
                c.setFechaNacimiento(fecha);
            } else {
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios que no pueden estar vacíos.");
                return;
            }

            controller.create(c);

            JOptionPane.showMessageDialog(this, "Cliente insertado correctamente.");
            cargarTabla();

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this,
                    ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente");
            return;
        }
        if (!txtIdCliente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Recuerda que el ID del cliente no se puede actualizar.");
            return;
        }

        try {

            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            Clientes c = controller.findById(id);

            String nombre = txtNombre.getText().trim();
            String ape1 = txtApe1.getText().trim();
            String ape2 = txtApe2.getText().trim();
            String dni = txtDni.getText().trim();
            String telefono = txtTelefono.getText().trim();
            String email = txtEmail.getText().trim();

            // VALIDAR CAMPOS OBLIGATORIOS
            if (nombre.isEmpty() || ape1.isEmpty() || dni.isEmpty() || telefono.isEmpty() || email.isEmpty()
                    || txtFecNac.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Hay campos obligatorios vacíos.");
                return;
            }

            if (!validarDatos(dni, telefono, email)) {
                return;
            }

            // FECHA
            Date fecha = validarFecha(txtFecNac.getText());

            if (fecha == null) {
                return;
            }

            // CALCULAR EDAD
            java.util.Calendar hoy = java.util.Calendar.getInstance();
            java.util.Calendar nacimiento = java.util.Calendar.getInstance();

            nacimiento.setTime(fecha);

            int edad = hoy.get(java.util.Calendar.YEAR) - nacimiento.get(java.util.Calendar.YEAR);

            // RESPONSABLE
            if (edad < 18) {
                int selResponsable = txtCodResponsable.getSelectedIndex();
                if (selResponsable == 0) {
                    JOptionPane.showMessageDialog(this, "Los menores necesitan responsable. Debes seleccionar uno.");
                    return;
                }

                Integer idResponsable = selResponsable;

                // BUSCAR RESPONSABLE
                Clientes responsable = controller.findById(idResponsable);

                // COMPROBAR QUE EXISTE
                if (responsable == null) {
                    JOptionPane.showMessageDialog(this, "El responsable no existe.");
                    return;
                }

                // CALCULAR EDAD DEL RESPONSABLE
                Calendar hoyResponsable = Calendar.getInstance();

                Calendar nacimientoResponsable = Calendar.getInstance();

                nacimientoResponsable.setTime(responsable.getFechaNacimiento());

                int edadResponsable = hoyResponsable.get(Calendar.YEAR) - nacimientoResponsable.get(Calendar.YEAR);

                // SI EL RESPONSABLE ES MENOR
                if (edadResponsable < 18) {
                    JOptionPane.showMessageDialog(this, "El responsable debe ser mayor de edad.");
                    return;
                }

                c.setCodResponsable(idResponsable);

            } else {
                c.setCodResponsable(null);
            }

            // ACTUALIZAR DATOS
            c.setNombre(nombre);
            c.setApe1cli(ape1);
            c.setApe2cli(ape2);
            c.setDni(dni);
            c.setTelefono(telefono);
            c.setEmail(email);
            c.setFechaNacimiento(fecha);

            controller.update(c);

            cargarTabla();

            JOptionPane.showMessageDialog(this, "Cliente actualizado correctamente");

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this,
                    ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar el cliente?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Integer id = (Integer) jTable1.getValueAt(fila, 0);

            controller.delete(id);

            cargarTabla();

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Cliente eliminado");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el cliente.\n" + "Tiene registros relacionados.");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

